  /// 구장명을 기준으로 구역 목록 가져오기
  static List<String> getZones(String stadiumName) {
    final seats = stadiumSeats[stadiumName];
    if (seats == null) return [];
    return seats.keys.toList();
  }

  /// 구장 + 구역을 기준으로 블럭 목록 가져오기
  static List<String> getBlocks(String stadiumName, String zoneName) {
    final seats = stadiumSeats[stadiumName];
    if (seats == null) return [];
    final blocks = seats[zoneName];
    if (blocks == null) return [];
    return blocks;
  }
